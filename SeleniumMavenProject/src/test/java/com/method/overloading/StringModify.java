package com.method.overloading;

public class StringModify {
	
	public static void main (String Args[]) {
		
		String a = "Selenium";
		System.out.println(a);
		
		a.concat("testing");
		System.out.println(a);
		
	/*	
		Once u define string you are not able to change string value 
		ie String is immutable*/
		
	}

}
